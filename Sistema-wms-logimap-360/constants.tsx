/**
 * Helper para criar registros de inventário com base na tabela fornecida.
 */
const createInventoryRecord = (
  street: string, 
  block: string, 
  level: string, 
  pos: string, 
  category: string, 
  description: string, 
  qty: number
): Product => {
  const parts = description.split(' - ');
  const code = parts[0]?.trim() || 'S/C';
  let rawName = parts[1]?.trim() || description;

  const is10m = category.toLowerCase().includes('10');
  const is30m = category.toLowerCase().includes('30');
  const suffix = is10m ? '10m' : (is30m ? '30m' : '');
  const formattedName = `Feltro ${rawName} ${suffix}`.trim();

  return {
    id: `${code}-${street}-${block}-${level}-${pos}-${Math.random().toString(36).substr(2, 4)}`,
    code,
    name: formattedName,
    category: category.toUpperCase(),
    description: description,
    unit: 'un',
    quantity: qty,
    minQuantity: 98, // Definido conforme solicitado
    address: { street, block, level, position: pos },
    supplier: 'Industria de Feltro Santa Fé',
    createdAt: new Date().toISOString().split('T')[0]
  };
};

